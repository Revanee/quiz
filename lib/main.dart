class Quiz extends StatefulWidget {
  @override
  State<StatefulWidget> createState() {
    return new QuizState();
  }
}

class QuizState extends State with SingleTickerProviderStateMixin {

  List<bool> answers = new List(questions.length);

  TabController controller;

  @override
  initState () {
    super.initState();
    controller = new TabController(
      vsync: this,
      length: questions.length
    );
  }

  @override
  dispose () {
    controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return new Scaffold(
      appBar: new AppBar(
        title: new Text(answers.fold(0, (i, e) => i + (e == null ? 0 : 1)).toString() + '/' + answers.length.toString()),
        actions: <Widget>[
          new Center(
            child: new Padding(
              padding: const EdgeInsets.all(8.0),
              child: new Text('time: 26:43'),
            ),
          ),
        ],
        bottom: new TabBar(
          tabs: questions.map((Map q) {
            return new Tab(
              child: new Text(q['answer'] ? 'true' : 'false'),
            );
          }).toList(),
          controller: controller,
        ),
      ),
      body: new TabBarView(
        controller: controller,
        children: questions.map((Map q) {
          return new Column(
              children: <Widget>[
                new Expanded(
                  child: makeQuestionText(q),
                ),
                new Padding(
                  padding: const EdgeInsets.all(20.0),
                  child: new Row(
                    children: makeButtons(answers[questions.indexOf(q)]),
                  )
                  // child: makeButtons(answers[controller.index]),
                )
              ],
            );
        }).toList(),
      ),
    );
  }

  Widget makeQuestionText(Map q) {
    return new Padding(
      padding: new EdgeInsets.all(20.0),
      child: new Text(q['questionText'])
    );
  }

  selectAnswer(bool ans) {
    setState(() => answers[controller.index] = ans);
    setState(() => controller.index += 1);
  }

  List<Widget> makeButtons(bool ans) {
    return <Widget>[
      new FlatButton.icon(
        onPressed: () {
          selectAnswer(true);
        },
        label: new Text('Vero'),
        icon: new Icon(Icons.check, 
          color: ans == null ? Colors.black : 
            ans ? Colors.green : Colors.black
        ),
      ),
      new FlatButton.icon(
          onPressed: () {
            selectAnswer(false);
          },
          label: new Text('Falso'),
          icon: new Icon(Icons.close, 
            color: ans == null ? Colors.black : 
              ans ? Colors.black : Colors.red
          )
        ),
    ];
  }
}

